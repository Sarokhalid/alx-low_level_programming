#include "3-calc.h"
/**
 * op_add - add
 * @a: integer
 * @b: integer
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub
 * @a: int
 * @b: int
 * Return: sub of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication
 * @a: int
 * @b: int
 * Return: mul a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div
 * @a: int
 * @b: int
 * Return: div of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod
 * @a: int
 * @b: int
 * Return: mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
